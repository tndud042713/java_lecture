package day03;

public class Test05 {
	public static void main(String[] args) {
		int su1;
		double su2;
		su1 = 5;
		++su1;
		System.out.println("++su1 = " + su1);
		su1 = 5;
		su1++;
		System.out.println("su1++ = " + su1);
		su2 = 11.2;
		++su2;
		System.out.println("++su2 = " + su2);
		su2 = 11.2;
		++su2;
		System.out.println("++su2 = " + su2);
	}
}
